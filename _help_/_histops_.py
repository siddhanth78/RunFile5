def histops(command):
    print()
    if command=="history":
        print("history : Display previously used commands.")
    elif command=="clearhistory":
        print("clearhistory : Clear all the previously used commands.")
    elif command=="searchhistory":
        print("searchhistory : Search and display previously used commands that match the search.\nSyntax: searchhistory::<keyword>")
    print()
    
